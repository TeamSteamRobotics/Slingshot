package org.usfirst.frc.team5119.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	 public final static int frontLeftMotor = 0;
	 public final static int frontRightMotor = 2;
	 public final static int backLeftMotor = 1;
	 public final static int backRightMotor = 3;
	 public final static int actuatorFeedback = 0;
	 public final static int actuatorMotor = 0;
	 public static final int shootMotor = 0;
}
